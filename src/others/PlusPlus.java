package others;

public class PlusPlus {

    public static void main(String[] args) {

        int x = 1;
        System.out.println(x); //1

        System.out.println(x++); //wyswietli 1 ale wartosc po dodaniu wyniesie 2

        System.out.println(++x); //doda 1 i wyswietli od razu 3


    }

}
